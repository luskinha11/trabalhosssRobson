/* Mutação de variável Em todos os exemplos
 até agora declaramos muitas variáveis, 
 algumas já populadas, outras não, porém em 
 nenhum momento realizamos a alteração do valor destas. 
 Como já declaramos as variáveis, não é mais necessário a 
 utilização da palavra reservada var para continuarmos a utilizá-las. 
 O primeiro ponto que devemos enfatizar, quando falamos sobre mudar o 
 valor de variáveis, também chamado de mutação de variável, é que a 
 lógica do script é percorrida linha por linha, da esquerda 
 para a direita. Tendo isto em mente é possível alterar o valor 
 de uma variável, apenas se esta linha de alteração estiver abaixo 
 da linha onde foi realizado a declaração da mesma, igualmente o novo 
 valor atribuído para uma variável poderá ser apresentado ao usuário ou 
 utilizado, apenas abaixo da linha onde foi realizado a alteração desta, 
 caso contrário a alteração não irá refletir para a sua página web. */